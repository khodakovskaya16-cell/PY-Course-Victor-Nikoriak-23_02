# =============================================================================
# data_utils/registry.py — Глобальний реєстр датасетів з кешуванням
# =============================================================================
# Принцип: кожен датасет завантажується ТІЛЬКИ ОДИН РАЗ при першому запиті
# та зберігається в пам'яті протягом сесії роботи дашборду.
# Це реалізовано через декоратор @lru_cache.
#
# Чому не завантажувати щоразу заново?
#   - CSV-файли займають час на читання та парсинг (~92 000 рядків для food)
#   - Callbacks Dash викликаються при кожній взаємодії користувача
#   - Кешування забезпечує швидку відповідь інтерфейсу
# =============================================================================

from functools import lru_cache
from .loader import load_food, load_markets, load_exchange, load_debt


# =============================================================================
# Реєстр датасетів
# =============================================================================

# Словник: назва → функція завантаження
# Використовуємо функції, а не датафрейми, щоб завантаження було ліниве
# (lazy loading) — тобто відбувається тільки при першому get_dataset() дзвінку
DATASETS = {
    "food":     load_food,
    "markets":  load_markets,
    "exchange": load_exchange,
    "debt":     load_debt,
}


@lru_cache(maxsize=None)
def get_dataset(name: str):
    """
    Повертає кешований датасет за назвою.

    Параметри:
        name — одне з: 'food', 'markets', 'exchange', 'debt'

    Повертає:
        pd.DataFrame — відповідний датасет

    Підіймає ValueError, якщо назва невідома.

    Примітка про lru_cache:
        @lru_cache кешує результат виклику функції на основі аргументів.
        Оскільки аргумент — рядок ('food'), однаковий при кожному виклику,
        датасет завантажується лише один раз і далі повертається з кешу.
        maxsize=None означає необмежений кеш (всі 4 датасети зберігаються).
    """
    if name not in DATASETS:
        raise ValueError(
            f"Датасет '{name}' не знайдено в реєстрі. "
            f"Доступні: {list(DATASETS.keys())}"
        )

    # Викликаємо функцію завантаження — результат буде збережений lru_cache
    return DATASETS[name]()
