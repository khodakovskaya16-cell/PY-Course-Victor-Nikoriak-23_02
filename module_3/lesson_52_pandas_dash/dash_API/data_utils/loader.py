# =============================================================================
# Допоміжна функція: перевірка наявності колонок
# =============================================================================

def _validate_columns(df: pd.DataFrame, required: list[str], source: str) -> None:
    """
    Перевіряє наявність обов'язкових колонок у датафреймі.

    Параметри:
        df       — датафрейм для перевірки
        required — список назв необхідних колонок
        source   — назва файлу (для повідомлення про помилку)

    Піднімає ValueError, якщо будь-яка колонка відсутня.
    Це захист від "сліпого" використання файлу без перевірки схеми.
    """
    missing = [c for c in required if c not in df.columns]
    if missing:
        raise ValueError(
            f"[{source}] Відсутні очікувані колонки: {missing}.\n"
            f"Наявні колонки: {list(df.columns)}"
        )


