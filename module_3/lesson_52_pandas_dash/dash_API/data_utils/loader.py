# 4. Завантаження показників зовнішнього боргу та макроекономіки
# =============================================================================

def load_debt() -> pd.DataFrame:
    """
    Завантажує датасет макроекономічних показників платіжного балансу України.

    Джерело: external-debt_ukr.csv (Світовий банк)
    Охоплення: 1989 — 2024 (річні дані)
    Кількість показників: 59

    ВАЖЛИВА МЕТОДОЛОГІЧНА ПРИМІТКА:
        Цей датасет містить 59 РІЗНИХ індикаторів платіжного балансу:
        - поточний рахунок
        - прямі іноземні інвестиції
        - перекази мігрантів
        - зовнішній борг у різних визначеннях
        - тощо
        Ці показники ВИМІРЮЮТЬСЯ В РІЗНИХ ОДИНИЦЯХ і мають різну
        економічну природу. НІКОЛИ не слід усереднювати або порівнювати
        їх безпосередньо між собою без методологічного обґрунтування.
        Дашборд дозволяє обрати ОДИН індикатор для аналізу.

    Структура після завантаження:
        рік         — рік спостереження (int)
        індикатор   — повна назва показника (англійською)
        код_індикатора — код Світового банку
        значення    — числове значення показника

    Одиниці вимірювання:
        Переважно USD (поточні ціни). Конкретна одиниця залежить від
        індикатора — перевіряйте назву перед інтерпретацією.
    """
    path = DATA_DIR / "external-debt_ukr.csv"

    df = pd.read_csv(path)

    _validate_columns(df, [
        "Year", "Indicator Name", "Indicator Code", "Value"
    ], source="external-debt_ukr.csv")

    # Перейменовуємо колонки
    df = df.rename(columns={
        "Year":           "рік",
        "Indicator Name": "індикатор",
        "Indicator Code": "код_індикатора",
        "Value":          "значення",
    })

    # Числовий тип для значень та року
    df["значення"] = pd.to_numeric(df["значення"], errors="coerce")
    df["рік"]      = pd.to_numeric(df["рік"],      errors="coerce").astype("Int64")

    # Сортуємо за роком
    df = df.sort_values(["індикатор", "рік"]).reset_index(drop=True)

    n_indicators = df["індикатор"].nunique()
    year_range   = f"{df['рік'].min()} — {df['рік'].max()}"

    print(f"[load_debt] Завантажено {len(df)} записів | "
          f"Індикаторів: {n_indicators} | Діапазон: {year_range}")

    return df
