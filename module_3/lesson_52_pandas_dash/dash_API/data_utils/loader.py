# 1. Завантаження цін на продукти харчування
# =============================================================================

def load_food() -> pd.DataFrame:
    """
    Завантажує та підготовлює датасет цін на продукти харчування WFP.

    Джерело: wfp_food_prices_ukr.csv (World Food Programme)
    Охоплення: 2014-03-15 — 2025-12-15
    Обсяг: ~92 000 записів, 45 товарів, 9 одиниць виміру

    Структура після завантаження:
        дата       — дата спостереження (datetime)
        товар      — назва товару
        одиниця    — одиниця виміру (KG, L, Loaf, 10 pcs тощо)
        ціна       — роздрібна ціна в гривнях (UAH)
        ціна_usd   — ціна в доларах США
        область    — назва адміністративної одиниці 1-го рівня (область)
        район      — назва адміністративної одиниці 2-го рівня (район)
        ринок      — назва ринку/міста
        id_ринку   — унікальний ідентифікатор ринку WFP
        широта     — географічна широта ринку
        довгота    — географічна довгота ринку
        категорія  — категорія товару
        id_товару  — числовий ідентифікатор товару
        валюта     — код валюти (UAH)

    Методологічна примітка:
        Запис з ринком "National Average" — це попередньо обчислений
        WFP середній показник по країні. Такі рядки мають пусті
        область/район/координати і ВИКЛЮЧАЮТЬСЯ з регіонального аналізу.
    """
    path = DATA_DIR / "wfp_food_prices_ukr.csv"

    # Завантажуємо сирі дані
    df = pd.read_csv(path)

    # Перевіряємо наявність усіх необхідних колонок
    _validate_columns(df, [
        "date", "admin1", "admin2", "market", "market_id",
        "latitude", "longitude", "category", "commodity", "commodity_id",
        "unit", "currency", "price", "usdprice"
    ], source="wfp_food_prices_ukr.csv")

    # Парсимо дату — формат '2014-03-15'
    df["date"] = pd.to_datetime(df["date"], errors="coerce")

    # Видаляємо рядки з відсутньою датою (якщо такі є)
    n_before = len(df)
    df = df.dropna(subset=["date"])
    if len(df) < n_before:
        print(f"[load_food] Видалено {n_before - len(df)} рядків з відсутньою датою")

    # Перейменовуємо колонки — з технічних назв у зрозумілі українські
    df = df.rename(columns={
        "date":         "дата",
        "admin1":       "область",
        "admin2":       "район",
        "market":       "ринок",
        "market_id":    "id_ринку",
        "latitude":     "широта",
        "longitude":    "довгота",
        "category":     "категорія",
        "commodity":    "товар",
        "commodity_id": "id_товару",
        "unit":         "одиниця",
        "currency":     "валюта",
        "price":        "ціна",
        "usdprice":     "ціна_usd",
    })

    # Забезпечуємо правильний числовий тип для цін
    df["ціна"]     = pd.to_numeric(df["ціна"],     errors="coerce")
    df["ціна_usd"] = pd.to_numeric(df["ціна_usd"], errors="coerce")

    # Сортуємо за датою для часових аналізів
    df = df.sort_values("дата").reset_index(drop=True)

    print(f"[load_food] Завантажено {len(df)} записів | "
          f"Товарів: {df['товар'].nunique()} | "
          f"Одиниць: {df['одиниця'].nunique()} | "
          f"Ринків: {df['id_ринку'].nunique()}")

    return df


