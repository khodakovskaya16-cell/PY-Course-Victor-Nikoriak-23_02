# 2. Завантаження довідника ринків (геопросторова прив'язка)
# =============================================================================

def load_markets() -> pd.DataFrame:
    """
    Завантажує довідник ринків WFP — геопросторову прив'язку market_id
    до координат та адміністративних одиниць.

    Джерело: wfp_markets_ukr.csv
    Обсяг: 268 ринків

    Структура після завантаження:
        id_ринку   — унікальний ідентифікатор WFP (ключ для join)
        ринок      — назва ринку
        область    — назва адміністративної одиниці 1-го рівня
        район      — назва адміністративної одиниці 2-го рівня
        широта     — географічна широта
        довгота    — географічна довгота

    Методологічна примітка:
        Цей файл є ДОВІДНИКОМ, а не аналітичним датасетом.
        Він використовується ТІЛЬКИ для збагачення цін координатами
        при побудові карт через join за id_ринку.
        1 ринок має відсутні координати — буде виключений з карти.
    """
    path = DATA_DIR / "wfp_markets_ukr.csv"

    df = pd.read_csv(path)

    _validate_columns(df, [
        "market_id", "market", "admin1", "admin2", "latitude", "longitude"
    ], source="wfp_markets_ukr.csv")

    # Перейменовуємо колонки
    df = df.rename(columns={
        "market_id": "id_ринку",
        "market":    "ринок",
        "admin1":    "область",
        "admin2":    "район",
        "latitude":  "широта",
        "longitude": "довгота",
    })

    # Залишаємо тільки потрібні колонки (відкидаємо countryiso3)
    df = df[["id_ринку", "ринок", "область", "район", "широта", "довгота"]]

    # Перевіряємо координати — ринки без них не можна нанести на карту
    n_missing_coords = df[["широта", "довгота"]].isnull().any(axis=1).sum()
    if n_missing_coords > 0:
        print(f"[load_markets] Увага: {n_missing_coords} ринків без координат "
              f"— вони будуть виключені з картографічного аналізу")

    print(f"[load_markets] Завантажено {len(df)} ринків | "
          f"Областей: {df['область'].nunique()}")

    return df


