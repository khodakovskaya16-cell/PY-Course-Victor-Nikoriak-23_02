# 3. Завантаження курсу обміну валюти
# =============================================================================

def load_exchange() -> pd.DataFrame:
    """
    Завантажує та підготовлює місячний ряд курсу UAH/USD (гривень за 1 долар).

    Джерело: exchange-rates_ukr.csv (FAO / FAOSTAT)
    Охоплення: 1990 — 2026 (місячні спостереження)

    Після завантаження залишаємо ТІЛЬКИ:
        - валюта: UAH (гривня, введена 1996 р.; до того — карбованець UAK)
        - серія: "Local currency units per USD" (стандартне визначення МВФ)
        - місяці: виключаємо 'Annual value' (не місячне спостереження)

    Структура після завантаження:
        дата       — перший день місяця спостереження (datetime)
        рік        — рік (int)
        місяць_назва — назва місяця (English)
        курс       — кількість гривень за 1 USD

    Методологічна примітка:
        - UAK (карбованець) виключено — це попередня валюта до 1996 р.,
          її не можна порівнювати з гривнею без перерахунку.
        - 'Standard local currency units per USD' — альтернативна серія FAO,
          яка відрізняється від офіційного курсу НБУ; виключаємо для
          консистентності.
        - Курс з цього файлу є орієнтовним. Для точного аналізу слід
          використовувати дані НБУ.
    """
    path = DATA_DIR / "exchange-rates_ukr.csv"

    df = pd.read_csv(path)

    _validate_columns(df, [
        "ISO Currency Code", "Element", "Year", "Months", "Value", "StartDate"
    ], source="exchange-rates_ukr.csv")

    # Фільтруємо: тільки гривня (UAH)
    df = df[df["ISO Currency Code"] == EXCHANGE_CURRENCY].copy()

    # Фільтруємо: тільки потрібна серія курсу
    df = df[df["Element"] == EXCHANGE_ELEMENT].copy()

    # Виключаємо річні підсумки — нам потрібні тільки місячні дані
    df = df[df["Months"] != "Annual value"].copy()

    # Парсимо дату з колонки StartDate (формат '1996-02-01')
    # StartDate — перший день відповідного місяця, що є зручним аналітичним індексом
    df["дата"] = pd.to_datetime(df["StartDate"], errors="coerce")

    # Видаляємо рядки з нечитабельною датою
    df = df.dropna(subset=["дата", "Value"])

    # Перейменовуємо та вибираємо лише потрібні колонки
    df = df.rename(columns={
        "Year":   "рік",
        "Months": "місяць_назва",
        "Value":  "курс",
    })

    df = df[["дата", "рік", "місяць_назва", "курс"]].copy()

    # Перевіряємо числовий тип
    df["курс"] = pd.to_numeric(df["курс"], errors="coerce")

    # Сортуємо за датою
    df = df.sort_values("дата").reset_index(drop=True)

    print(f"[load_exchange] Завантажено {len(df)} місячних спостережень | "
          f"Діапазон: {df['дата'].min().date()} — {df['дата'].max().date()} | "
          f"Курс: від {df['курс'].min():.2f} до {df['курс'].max():.2f} UAH/USD")

    return df


