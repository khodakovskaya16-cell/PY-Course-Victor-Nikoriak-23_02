# =============================================================================
# app.py — Точка входу застосунку Dash
# =============================================================================
# Цей файл:
#   1. Ініціалізує об'єкт Dash
#   2. Будує та підключає головний лейаут
#   3. Реєструє ВСІ callbacks (тільки через tabs_callbacks)
#   4. Запускає сервер розробки
#
# Архітектура:
#   app.py
#   └── layouts/main_layout.py  → побудова HTML-структури
#   └── callbacks/tabs_callbacks.py → вся інтерактивна логіка
#
# Для запуску:
#   cd dash_API
#   python app.py
#   Відкрийте: http://localhost:8055
# =============================================================================

from dash import Dash

# Імпортуємо функції побудови лейауту та реєстрації callbacks
from layouts.main_layout import create_layout
from callbacks.tabs_callbacks import register_tab_callbacks

# ─── Ініціалізація застосунку ────────────────────────────────────────────────

# suppress_callback_exceptions=True — дозволяє callbacks посилатися на компоненти,
# що рендеряться динамічно (всередині tab-content).
# Без цього Dash підняв би помилку: "ID not found in layout"
app = Dash(
    __name__,
    suppress_callback_exceptions=True,
    title="Аналіз цін в Україні",
    update_title="Оновлення...",
)

# ─── Побудова лейауту ────────────────────────────────────────────────────────
# create_layout() завантажує дані для побудови фільтрів (один раз через lru_cache)
# та повертає повну html.Div структуру сторінки.
app.layout = create_layout()

# ─── Реєстрація callbacks ─────────────────────────────────────────────────────
# Всі callbacks дашборду реєструються в одній функції.
# Tabs-архітектура: callback рендерить контент активної вкладки.
register_tab_callbacks(app)

# ─── Запуск сервера ───────────────────────────────────────────────────────────

if __name__ == "__main__":
    app.run(
        debug=True,   # True = hot-reload + повідомлення про помилки в браузері
        port=8055,    # Порт: http://localhost:8055
    )
